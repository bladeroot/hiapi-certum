    /**
     * Returns an array with descriptions of errors that have occured.
     * 
     * The returned array is an array of arrays containing the following keys:
     * code, number, text, where code is a success code, number is a error
     * number and text is a description of an error.
     * 
     * @param string $lang
     * @param int $status
     * @param array $errors
     * @return array
     */
    public function getText($lang, $status, $errors) {
        $texts = array();
        if (empty($errors)) {
            $t = isset($this->texts_status[$lang][$status]) ? 
                    $this->texts_status[$lang][$status] : '';
            $texts[] = array(
                'code' => $status,
                'number' => NULL,
                'text' => $t
            );
        } else {
            foreach ($errors as $e) {
                $t = isset($this->texts_error[$lang][$e->errorCode]) ? 
                        $this->texts_error[$lang][$e->errorCode] : '';
                $v = empty($e->value) ? '' : ' ['.$e->value.']';
                $texts[] = array(
                    'code' => $status,
                    'number' => $e->errorCode,
                    'text' => $t.$v
                );
            }
        }
        return $texts;
    }
